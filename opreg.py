#Arithmetic
a = 10
b = 3

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)
print(a ** b)
print(a // b)

# Assignment Operators
x = 5
print(x)

x += 3
print(x)

x -= 2
print(x)


#Comparison operators
p = 7
q = 10

print(p == q)   
print(p != q)   
print(p > q)   
print(p < q)   
print(p >= q)   
print(p <= q)  

#logical operators
a = True
b = False

print(a and b)  
print(a or b)   
print(not a)    

#Identity Operators
x = ["apple", "banana"]
y = x
z = ["apple", "banana"]

print(x is y)    
print(x is z)    
print(x is not z)  

#Membership Operators
fruits = ["apple", "banana", "cherry"]

print("apple" in fruits)      
print("grape" in fruits)      
print("banana" not in fruits) 


# Bitwise Operators
a = 5   
b = 3    

print(a & b)   
print(a | b)   # OR → 7   (0111)
print(a ^ b)   # XOR → 6  (0110)
print(~a)      # NOT → -6
print(a << 1)  # Left shift → 10 (1010)
print(a >> 1)  # Right shift → 2 (0010)


